	<p>aat Beverly keluar dari mobilnya sekitar pukul 23.30 malam, seseorang mendorongnya ke dalam mobil. Dia
		mendorongnya melewati kursi pengemudi dan masuk ke dalam kursi penumpang. Pada saat yang sama, seseorang masuk
		ke kursi belakang dan menodongkan pistol ke leher Beverly.<br /><br />"Sekarang kita akan pergi," kata pria yang
		duduk di kursi pengemudi.<br /><br />Dia memundurkan mobil keluar dari halaman rumah Beverly di Pulau Trinidad,
		Karibia, dan ketiganya berkendara dalam keheningan.<br /><br />Kemudian Beverly pun mulai berbicara. "Apakah
		kalian akan membunuhku?" katanya.<br /><br />"Jika kamu tidak melihat kami, kamu tidak akan dibunuh," kata
		pengemudi itu.<br /><br />Beverly telah melihat bahwa pengemudi itu mengenakan hoodie. Namun, ia tidak
		melihatnya lagi.<br /><br />"Apakah kamu tahu sudah berapa lama kami mengikutimu?" tanya pengemudi
		itu.<br /><br />Beverly tidak menjawab. Dua setengah jam telah berlalu sejak ia meninggalkan University of the
		Southern Caribbean, institusi Masehi Advent Hari Ketujuh di mana ia bekerja dan pada saat yang sama sedang
		menempuh pendidikan untuk meraih gelar master dalam bidang psikologi. Sebelumnya, ia mengantar seorang teman
		kuliahnya pulang ke rumah, dan mereka berdua sempat berhenti untuk makan di tengah perjalanan. Setelah mengantar
		temannya, dia kemudian pulang ke rumahnya sendiri.<br /><br />"Siapa gadis yang kamu antar tadi?" tanya sang
		pengemudi. <br /><br />"Teman kuliah," kata Beverly. <br /><br />"Kami sudah lama mengikutimu," kata sang
		pengemudi.<br /><br />Pria di kursi belakang menarik tangan Beverly ke belakang kursi dan mencoba melepaskan
		cincin kawinnya.<br />'Lepaskan," kata pengemudi itu. Pria itu melepaskan tangan Beverly.<br /><br />Pengemudi
		itu bertanya kepada Beverly apakah dia pernah mendengar tentang serangkaian pembunuhan baru-baru ini di pulau
		itu. Dia mengatakan bahwa seorang temannya telah dibunuh, dan dia berencana untuk menggunakan mobil Beverly
		untuk membalas dendam.<br /><br />"Bisakah Anda membiarkan saya keluar dari mobil?" Beverly
		bertanya.<br /><br />"Tidak, kamu ikut dengan kami," katanya.<br /><br />Namun beberapa saat kemudian dia tampak
		berubah pikiran.<br /><br />"Apakah kamu memiliki seseorang yang bisa kami hubungi?" katanya.<br /><br />"Suami
		saya."<br /><br />"Telepon dia dan kita lihat apakah dia bisa menjemputmu."<br /><br />Entah bagaimana, pria di
		kursi belakang itu berhasil mendapatkan tas Beverly. Dia membukanya, mengeluarkan ponsel Beverly, dan
		menyerahkannya kepadanya.<br /><br />Beverly menelepon suaminya, tetapi tidak ada jawaban.<br /><br />Pria yang
		duduk di kursi belakang merebut kembali ponsel itu dan mengatakan keraguannya bahwa Beverly telah menelepon
		suaminya. "Dia bahkan tidak menyimpan nomor ini di panggilan cepat," katanya.<br /><br />Perjalanan di malam
		hari itu pun berlanjut. "Saya merasa takut," kata Beverly. "Bolehkah saya memutarkan lagu dari CD?" Namun ketika
		ia menekan tombol play, tidak ada CD yang keluar dari alat pemutarnya. Dia tidak mengerti mengapa. Pagi harinya,
		Beverly sempat mendengarkan musik rohani dalam perjalanannya ke universitas. Dia bernyanyi mengikuti lagu,
		"Yesus, peganglah kemudi."<br /><br />Sekarang Beverly bertanya-tanya apakah mungkin disket itu terjatuh ke
		lantai. Dia meraba-raba dengan tangannya dan menemukan sebuah disket. "Ada di sini, di bawah sana," katanya.
		"Biarkan saya memasukkannya ke dalam."<br /><br />Sebuah lagu mulai diputar. Ternyata bukan disket yang
		diinginkan Beverly. la tidak tahu dari mana datangnya disket itu. Lalu, terdengar suara seseorang bernyanyi,
		"TUHAN, aku percaya kepada-Mu."<br /><br />Orang yang duduk di belakang mengeluh. Pengemudi itu sedang sibuk
		dengan urusannya. la berkata, "Kami akan mengganti plat nomor Anda, dan besok polisi akan segera menemukan
		mobilmu."<br /><br />Dia ingin melepaskan Beverly. Tetapi Beverly tidak ingin diturunkan tanpa uang dalam
		kegelapan. "Saya tidak punya uang," katanya. "Bagaimana saya bisa pulang ke rumah?"<br /><br />Pengemudi itu
		menyuruh temannya untuk memberikan sejumlah uang kepada Beverly.<br /><br />"Berapa banyak? 20 dolar?" kata
		temannya. "Saya punya 1.000 dolar di dompet yang ada di belakang," kata Beverly. Hari itu, dia telah mengambil
		uang tabungannya dari bank dan akan membawanya pulang untuk belanja. Dia yakin bahwa pria di kursi belakang
		telah menemukan uangnya, dan dia ingin sang pengemudi mengetahuinya.<br /><br />"Berikan dia 100 dolar," kata
		sang sopir. Beverly menerima uang tersebut, mobil berhenti, dan dia diperintahkan untuk keluar. Kemudian dia
		menyeberang jalan dan memanggil taksi, yang membawanya pulang.<br /><br />Sampai hari ini, Beverly tidak tahu
		mengapa Tuhan mengizinkan cobaan yang mengerikan itu. Tetapi ketika dia mengingat kembali apa yang terjadi,
		sepertinya Tuhan berusaha menarik perhatiannya, dengan berkata, "Percayalah kepada-Ku dalam
		hidupmu."<br /><br />Satu hal, pada pagi hari sebelum perampokan mobil itu, ia telah bernyanyi, "Yesus,
		peganglah kemudi." Tetapi kemudian dia menghabiskan sepanjang hari seperti yang dia lakukan di hari-hari
		lainnya, berusaha mengendalikan semua kehidupannya tanpa pertolongan Tuhan.<br /><br />Ini merupakan ketiga
		kalinya ia kehilangan mobil. Pertama kali, mobilnya dicuri di depan rumahnya. Kedua kalinya, mobilnya hancur
		dalam sebuah kecelakaan.<br /><br />Sepertinya lebih dari sekadar kebetulan, saat pulang ke rumah setelah
		perampokan mobil, sebuah lagu diputar di audio taksi, "Berapa kali aku harus melewati hal yang sama untuk
		menunjukkan betapa aku mencintaimu?"<br /><br />Polisi tidak pernah menemukan mobil Beverly. Tetapi dia tidak
		mengeluh. la mengatakan bahwa perampokan mobil itu merupakan titik balik dalam hidupnya. Sekarang dia telah
		memutuskan untuk memercayai Tuhan dalam setiap aspek kehidupannya. "Banyak orang yang mobilnya dicuri kemudian
		dibunuh," katanya. "Saya tidak tahu mengapa saya masih hidup. Sebagai orang Kristen, kita harus memercayai Tuhan
		dalam segala situasi."<br /><br />Kisah misi ini memberikan pandangan dalam kehidupan di Trinidad dan di
		University of the Southern Caribbean, yang menerima sebagian dari Persembahan Sabat Ketiga Belas tahun 2018
		untuk membangun sebuah gereja universitas. Universitas tempat Beverly bekerja saat ini juga menerima bagian dari
		Persembahan Sabat Ketiga Belas tahun 2021 membuka pusat pengaruh untuk melatih para
		misionaris.<br /><br />Terima kasih atas Persembahan Sabat Ketiga Belas Anda yang telah membantu mengajar
		orang-orang untuk memercayai Tuhan di Trinidad dan di seluruh dunia.</p>

