	<p>ada hari pertama sekolah, Guru Antonia berdiri di depan kelasnya di kelas tiga dengan sebuah kotak kue yang
		dibungkus dengan kertas kado berwarna putih, merah muda, hitam, dan abu-abu. Anak-anak memperhatikan dengan
		penuh rasa ingin tahu saat dia membagikan potongan kertas berwarna kuning dan hijau.<br /><br />"Ini adalah
		kapsul waktu," kata Antonia sambil menunjuk ke arah kotak itu.<br /><br />Sambil menunjuk kertas-kertas itu, ia
		menambahkan, "Di kertas kalian, tuliskan nama kalian dan apa yang ingin kalian doakan untuk tahun ajaran
		ini."<br /><br />la mengatakan bahwa kertas-kertas itu akan dimasukkan ke dalam kotak, dan kotak itu akan
		ditutup rapat. Pada akhir tahun, anak-anak akan mengadakan sebuah perayaan untuk membuka kotak tersebut dan
		melihat bagaimana Tuhan menjawab doa-doa mereka.<br /><br />Anak-anak dengan penuh semangat menuliskan
		permintaan.doa mereka di Sekolah Dasar Masehi Advent Hari Ketujuh Ebenezer.<br /><br />Antonia mengatakan bahwa
		ia juga akan memasukkan permohonan doa ke dalam kotak tersebut.<br /><br />"Ini adalah hal yang serius bagi
		saya," katanya. "Saya percaya Tuhan dapat mengubah hidup saya."<br /><br />Anak-anak menuliskan hal-hal yang
		mereka inginkan.<br /><br />Setelah kapsul waktu ditutup, tangan seorang anak laki-laki
		terangkat.<br /><br />"Bolehkah kami menamai kapsul waktu ini?" tanyanya dengan penuh
		semangat.<br /><br />Ketika sang guru menganggukkan kepala, dia berseru, "Jerry, kapsul waktu!"<br /><br />Semua
		anak bertepuk tangan dan bersorak.<br /><br />Kemudian anak laki-laki itu berdoa di depan kotak
		itu.<br /><br />"Terima kasih untuk Jerry," katanya.<br /><br />Kapsul waktu adalah sebuah alat pengajaran yang
		Antonia dengar dari seorang guru sekolah Advent lainnya tepat beberapa hari sebelum pembukaan tahun ajaran baru
		di Dominika. la menyukai ide untuk memasukkan pelajaran rohani ke dalam kegiatan sosial yang biasanya menjadi
		ciri khas hari pertama sekolah di SD Advent Ebenezer.<br /><br />Setelah beberapa pekan dan bulan berlalu,
		anak-anak bergiliran memegang Jerry dan berdoa.<br /><br />"Tolonglah kami untuk mencapai cita-cita kami," salah
		satu anak berdoa.<br /><br />"Tolong wujudkanlah keinginan kami," doa yang lain.<br /><br />Ketika para tamu
		seperti kepala sekolah, pendeta, dan pemimpin gereja mengunjungi kelas, anak-anak meminta mereka untuk memegang
		kotak itu dan mendoakan permintaan mereka.<br /><br />Di pertengahan tahun ajaran, seorang gadis kecil
		mengangkat tangannya ke atas.<br /><br />"Guru! Guru! Ada yang ingin saya sampaikan," katanya.<br /><br />Dia
		mengatakan bahwa Tuhan telah menjawab permintaan doanya. la telah menulis di selembar kertas bahwa ia ingin bisa
		membaca dengan lebih baik, dan sekarang ia sudah melakukannya.<br /><br />Kemudian sejumlah tangan kecil
		terangkat. Anak-anak berbicara bahwa mereka berdoa untuk mendapatkan nilai yang tinggi dan mereka
		mendapatkannya.<br /><br />Dua anak mengatakan bahwa mereka dapat menghabiskan waktu bersama ayah mereka, yang
		tinggal di pulau lain.<br /><br />Seorang anak perempuan mengatakan bahwa ia telah berdoa untuk mendapatkan
		saudara laki-laki atau perempuan, dan seorang bibi yang memiliki bayi telah pindah ke rumah yang dekat dengan
		rumahnya.<br /><br />Ketika puji-pujian anak-anak itu berakhir, Antonia menundukkan kepalanya dan memanjatkan
		doa syukur. la mengutip dari Markus 9: 23.<br /><br />"Seperti yang Yesus katakan, 'Bagi orang yang percaya,
		segala sesuatu mungkin terjadi," katanya.<br /><br />Sebagian dari Persembahan Sabat Ketiga Belas triwulan ini
		akan membantu Sekolah Dasar Masehi Advent Hari Ketujuh Ebenezer untuk membangun gedung yang lebih besar. Sekolah
		dibuka yang pada tahun 1976 ini secara konsisten masuk dalam daftar 10 besar dari 62 sekolah dasar di Dominika.
		Namun, gedung sekolah ini penuh sesak dengan 160 anak dari taman kanak-kanak hingga kelas empat. Beberapa blok
		jauhnya, 40 siswa kelas lima dan enam lainnya belajar di ruang kelas darurat di gedung terpisah. Banyak orang
		tua yang ingin menyekolahkan anaknya di sekolah ini, tetapi tidak ada tempat. Terima kasih telah merencanakan
		persembahan yang murah hati pada tanggal 28 September.</p>

