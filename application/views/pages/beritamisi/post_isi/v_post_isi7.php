	<p>ilinton minum alkohol untuk pertama kalinya setelah pulang dari gereja pada suatu hari Sabat di Bogota,
		Kolombia. Saat itu ia berusia 14 tahun, dan ia pun menyerah pada tekanan dari teman-temannya.<br /><br />Setelah
		minuman pertama, dia minum hampir setiap hari selama 24 tahun berikutnya.<br /><br />Wilinton pindah dari rumah
		orang tuanya saat berusia 17 tahun. Kemudian dia pindah dengan pacarnya dan tinggal bersamanya selama 21 tahun.
		Mereka dikaruniai dua orang putra. Dia minum alkohol di tiap ada kesempatan. Karena kebiasaannya minum-minum,
		dia tidak bisa bekerja.<br /><br />Suatu hari, seorang teman ayahnya datang ke rumahnya dan mendapati dia dalam
		keadaan mabuk.<br /><br />"Wilinton, saya kenal ayahmu," kata teman itu, yang bernama Jaime. "Kamu dapat kembali
		kepada Tuhan." "Apakah Anda seorang Advent?" Wilinton bertanya, bicaranya tidak jelas. "Ya, saya seorang
		Advent," kata Jaime.<br /><br />"Jika Anda seorang Advent, nyanyikanlah untuk saya Lagu Pujian No. 500," kata
		Wilinton. Nyanyian pujian itu adalah "Far Beyond the Sun."<br /><br />Jaime menyanyikan nyanyian pujian itu, dan
		Wilinton menangis. Ketika lagu itu berakhir, Wilinton membacakan ayat Alkitab favoritnya. Itu adalah Yohanes
		3:16.<br /><br />Hari itu menjadi awal dari sebuah persahabatan yang baru.<br /><br />Selama tiga tahun
		berikutnya, Jaime terus mengunjungi Wilinton dan mengatakan kepadanya, "Tolong, kembalilah kepada
		Tuhan."<br /><br />Wilinton selalu memberikan jawaban yang sama. "Ya," katanya. "Jangan khawatir. Saya akan
		kembali." Tetapi dia tidak melakukannya.<br /><br />Jaime menawarkan pelajaran Alkitab, tetapi Wilinton tidak
		tertarik. la menyukai Jaime, tetapi ia tidak ingin menghabiskan waktu bersama Tuhan.<br /><br />Tetap saja, ia
		masih mempertahankan Sabat hari ketujuh kepada istri dan kedua putranya, yang pergi ke gereja pada hari
		Minggu.<br /><br />"Kamu tidak perlu pergi ke gereja itu," katanya. "Itu hanya membuang-buang waktu. Jika kamu
		ingin menghabiskan waktumu dengan bijak, kamu harus pergi ke Gereja Masehi Advent Hari
		Ketujuh."<br /><br />Karena Wilinton terus minum-minum tanpa penghasilan tetap, dia akhirnya terjerat banyak
		utang.<br /><br />Suatu malam, Jamie tiba di rumah Wilinton bersama seorang pendeta Advent dan beberapa anggota
		gereja lainnya. Pintu depan terbuka, dan mereka masuk ke dalam rumah sebelum Wilinton
		menyadarinya.<br /><br />Pendeta menyampaikan khotbah selama lima menit, tetapi Wilinton tidak mendengar apa
		yang dikatakan pendeta. la merasa seperti mendengar suara Roh Kudus. Khotbah itu menyentuh
		hatinya.<br /><br />Kemudian Jamie mengundangnya ke pertemuan penginjilan yang baru saja dimulai di gereja
		Advent.<br /><br />"Ibumu meminta saya untuk memberitahukan kepadamu, 'Datanglah kepada Tuhan,"
		katanya.<br /><br />Setelah para pengunjung pergi, Wilinton memutuskan untuk tinggal di rumah bersama
		keluarganya.<br /><br />Kemudian putranya yang berusia 12 tahun menghampirinya.<br /><br />"Ayah, engkau
		mengatakan kepada saya bahwa Gereja Masehi Advent Hari Ketujuh adalah gereja yang benar," katanya. "Tolong bawa
		saya ke gereja itu."<br /><br />Kata-kata itu menghancurkan hati Wilinton. la memutuskan untuk membawa
		keluarganya ke pertemuan-pertemuan penginjilan di gereja Advent.<br /><br />Pemaparan malam itu mengejutkan
		Wilinton. Dia mengira bahwa itu adalah tentang dirinya. Ia berpikir, "Bagaimana pendeta ini tahu begitu banyak
		tentang saya?" Kemudian ia mulai menangis. Ia berpikir tentang kecanduan alkohol dan utang-utangnya. Hidupnya
		yang penuh dengan dosa sangat membebaninya.<br /><br />Ketika pendeta itu melakukan panggilan ke altar, Wilinton
		berkata pada dirinya sendiri, "Saya tidak bisa maju ke depan. Ketika saya pergi, saya akan pergi ke bar untuk
		minum." Dia memutuskan untuk tidak maju ke depan.<br /><br />Namun sebelum dia menyadarinya, dia telah berdiri
		di depan bersama 17 orang lainnya. Dia berdoa dalam hati, "Tuhan, saya tidak bisa berada di sini. Engkau tahu
		bahwa saya akan minum-minum malam ini."<br /><br />Setelah pertemuan itu, ia pergi ke bar. Sambil minum, ia
		merenungkan khotbah tersebut. Ia merasakan sesuatu di dalam dirinya yang berbeda.<br /><br />Tiga pekan berlalu,
		dan dia terus minum. Setiap kali ia minum, ia merasakan bahwa Roh Kudus berkata, "Aku akan menolongmu untuk
		berhenti minum jika kamu mengambil keputusan yang tegas untuk berhenti."<br /><br />Pada pekan keempat, ia
		berkata pada dirinya sendiri, "Saya tidak bisa terus melakukan ini. Saya harus membuat keputusan, untuk Tuhan
		atau untuk alkohol."<br /><br />Di akhir pekan, pada Sabtu malam, dia memilih Tuhan. Ia berdoa, "Tuhan, saya
		tidak ingin minum lagi." Di pagi harinya, ia berdoa lagi, "Tuhan, tolonglah saya hari ini, hari demi hari, untuk
		berhenti minum."<br /><br />Secara mengejutkan, dia tidak minum sepanjang hari itu. Itu adalah hari pertama
		kemenangannya sejak dia mulai minum pada usia 14 tahun. Dia merasa terkejut, "Saya pikir saya tidak akan pernah
		berhenti minum, tetapi Tuhan telah menolong saya."<br /><br />Ketika Jaime mendengar berita itu, dia sangat
		senang. Wilinton telah kembali kepada Tuhan.<br /><br />Kisah misi ini memberikan pandangan dari dalam tentang
		kehidupan di Kolombia dan tantangan-tantangan misi di sana. Sebagian dari Persembahan Sabat Ketiga Belas
		triwulan ini akan membantu membuka dua pusat pengaruh untuk menolong anak-anak bermasalah mengalami
		penyalahgunaan alkohol dan obat-obatan di Kolombia. Pusat-pusat pengaruh tersebut akan berada di Kota
		Buenaventura dan Puerto Tejada. Terima kasih telah merencanakan persembahan yang murah hati pada tanggal 28
		September.</p>

