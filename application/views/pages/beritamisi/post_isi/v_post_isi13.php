	<p>rickson memiliki sebuah kisah yang mungkin hanya sedikit orang yang mendengarnya. Dia menceritakan sebagian dari
		kisah tersebut kepada ibunya. Dia menceritakan seluruh kisahnya kepada istrinya. Ketiga anak perempuannya yang
		masih kecil tidak pernah mendengar cerita itu. Almarhum ayahnya pun tidak pernah mendengar
		kisahnya.<br /><br />Erickson, yang adalah seorang pendeta dan pemimpin Gereja Masehi Advent Hari Ketujuh di
		Kosta Rika, enggan membagikan kisahnya karena khawatir orang-orang tidak akan memercayainya. Namun setelah
		mendengar sejumlah kesaksian yang luar biasa ketika ia membantu Misi Advent mengumpulkan cerita untuk laporan
		triwulanan misi di Kosta Rika, ia menceritakan kisahnya kepada Misi Advent. Jadi, pada hari Sabat ini, Anda akan
		menjadi salah seorang yang pertama mendengar seluruh kisah-nya. Kisah yang mengubah hidupnya. Kisah tersebut
		membawanya menjadi seorang Masehi Advent Hari Ketujuh.<br /><br />Terdapat hanya dua sekolah di kota tempat
		Erickson dibesarkan di Guatemala: sekolah umum dan sekolah Advent.<br /><br />Sang ayah mengirim Erickson ke
		sekolah umum saat kelas satu, tetapi anak itu tidak pernah bertemu dengan gurunya selama setahun penuh. Sang
		guru sedang mengikuti pelatihan, kepala sekolah menjelaskan. Kepala sekolah terkadang datang ke kelas satu untuk
		memberikan tugas. Tetapi tidak ada yang mengajar pelajaran membaca, matematika, atau mengeja kepada anak-anak
		pada tahun itu. Untungnya, Erickson telah mempelajari mata pelajaran tersebut di taman kanak-kanak, jadi dia
		bisa menyelesaikan tugas pekerjaan rumah yang diberikan. Namun banyak teman sekelasnya yang gagal di kelas
		satu.<br /><br />Ayahnya merasa kecewa dengan sistem sekolah umum setelah satu tahun berjalan, dan dia
		memindahkan anaknya ke sekolah Advent untuk kelas dua. Di sanalah ia belajar tentang Tuhan.<br /><br />Sebelum
		tidur di malam hari, Erickson berlutut di samping tempat tidurnya untuk berdoa.<br /><br />"Tuhan, tolonglah
		saya untuk bisa membuat keputusan-keputusan yang baik dalam hidup saya," doanya. "Tolonglah saya di sekolah.
		Tolonglah keluarga saya dan khususnya ayah saya."<br /><br />Keluarganya termasuk golongan miskin. Ayahnya
		bekerja sebagai sopir bus yang jauh dari rumah, dan Erickson hanya bertemu dengannya beberapa kali setiap tahun.
		Terlepas dari kemiskinan mereka, sang ayah selalu berhasil mengumpulkan dana untuk membiayai pendidikan Erickson
		di sekolah Advent.<br /><br />Ketika Erickson berdoa di malam hari, dia berharap bisa bertemu lebih sering
		dengan ayahnya.<br /><br />"Tolong lindungi ayah dan ibu agar mereka dapat mencari uang untuk kebutuhan kami,"
		doanya. "Amin."<br /><br />Suatu malam, saat Erickson berusia 11 atau 12 tahun, ia berdoa dan kemudian berbaring
		di tempat tidurnya di kamar yang gelap. Matanya terbuka, tetapi ia tidak dapat melihat apa pun dalam
		kegelapan.<br /><br />Tiba-tiba, sebuah cahaya terang namun lembut muncul di langit-langit di atas kepalanya.
		Itu bukanlah cahaya lampu; Erickson telah mematikannya sebelum berdoa. Cahaya itu menerangi ruangan. Erickson
		tidak merasa takut. Dia merasa damai dan tenang.<br /><br />Tiba-tiba, sesosok makhluk bercahaya muncul dari
		lampu ke lantai. Erickson melihat tangan, kaki, tubuh, dan kepala. Tetapi dia tidak dapat melihat mata, telinga,
		hidung, mulut, atau bagian wajah lainnya. Kepala itu dikelilingi oleh cahaya lembut. Makhluk itu berlutut di
		tempat yang sama di mana anak laki-laki itu baru saja<br /><br />berdoa di samping tempat tidur. Dia menyatukan
		kedua tangannya dan menundukkan kepalanya.<br /><br />Erickson memperhatikan, dan bertanya-tanya apa yang akan
		terjadi selanjutnya. Doa Erickson biasanya berlangsung selama lima atau 10 menit, tetapi doa ini berakhir lebih
		cepat. Makhluk itu berdoa selama 60 hingga 90 detik. Kemudian dia berdiri dan meletakkan satu tangan di kepala
		Erickson dan tangan lainnya di dadanya. Sukacita memenuhi hati Erickson. Dia membayangkan bahwa dia sedang
		melihat Yesus.<br /><br />Makhluk itu tidak mengatakan apa-apa. Setelah menyentuh kepala dan dada anak itu
		selama beberapa detik, ia bangkit kembali ke arah cahaya lembut yang masih bersinar di langit-langit. Kemudian
		cahaya itu padam, dan kamar tidurnya kembali menjadi gelap.<br /><br />Kedamaian dan ketenangan menyelimuti
		Erickson. Dia hanya merasakan kebahagiaan di dalam kamar. Dia segera tertidur.<br /><br />Keesokan harinya,
		Erickson menceritakan pengalaman itu kepada ibunya. la merasa lebih dekat dengan Yesus daripada
		sebelumnya.<br /><br />Tetapi itu bukanlah akhir dari kisahnya. Beberapa bulan kemudian, Erickson bermimpi
		tentang hari Sabat. la sudah menghadiri gereja pada hari Minggu sejak ia masih kecil, dan ia terus melakukannya
		untuk membahagiakan ayahnya. Namun setelah ia masuk sekolah Advent, ia juga mulai pergi ke gereja Advent pada
		hari Sabat.<br /><br />Dalam mimpinya, Erickson sedang berdiri di gereja ayahnya selama kebaktian. Tiba-tiba,
		patung-patung orang kudus yang berjejer di dinding gereja mulai bergerak. Tangan mereka bergerak, dan kaki
		mereka bergerak. Sambil tertawa, mereka mengejar anak laki-laki yang ketakutan itu di sekitar gereja. Erickson
		dapat melihat jemaat lain di dalam gereja, tetapi mereka bersikap seolah-olah tidak ada yang
		aneh.<br /><br />Tiba-tiba, Erickson melihat cahaya terang namun lembut di pintu gereja. Itu adalah cahaya yang
		sama dengan yang dia lihat di kamar tidurnya beberapa bulan sebelumnya.<br /><br />Erickson merasa lega. la
		merasakan Yesus sudah dekat dan pertolongan sedang dalam perjalanan.<br /><br />Kemudian makhluk yang telah
		berdoa di kamar Erickson muncul dari cahaya di pintu gereja. Dia memegang tangan anak itu dan menuntunnya keluar
		dari gereja. Sekali lagi, anak itu dapat melihat tangan, kaki, tubuh, dan kepala, tetapi ia tidak dapat melihat
		raut wajahnya.<br /><br />Begitu sampai di luar, makhluk itu berdiri di antara Erickson dan gereja. Dia tidak
		mengucapkan sepatah kata pun. Sebaliknya, ia menunjuk ke arah gereja dan kemudian mengguncangnya, memperingatkan
		anak laki-laki itu untuk tidak beribadah di sana lagi.<br /><br />Keesokan paginya, Erickson tidak menceritakan
		mimpinya kepada ibu. la takut sang ibu akan memberitahukannya kepada sang ayah. Ayah ingin dia menjadi seorang
		pendeta, dan dia takut akan reaksi ayahnya.<br /><br />Tetapi setelah mimpi itu, Erickson tidak pernah beribadah
		lagi pada hari Minggu. la merasakan bahwa Yesus sedang membimbingnya ke jalan kebenaran, dan ia ingin
		mengikuti-Nya. Sang ayah, yang hanya pergi ke gereja pada hari Natal dan Paskah, tidak pernah meminta anak itu
		untuk kembali ke gerejanya.<br /><br />Sekitar setahun setelah mimpi itu, Erickson memutuskan untuk memberikan
		hatinya kepada Yesus dan dibaptis. Dia berusia 13 tahun ketika dia bergabung dengan Gereja Masehi Advent Hari
		Ketujuh.<br /><br />Saat ini, Erickson Bala adalah seorang pendeta dan pemimpin gereja yang mengawasi
		penjangkauan misi di tanah kelahirannya, Kosta Rika. Dia memutuskan untuk membuka rahasia masa lalunya sebagai
		pengingat pada hari Sabat Ketiga Belas ini bahwa Tuhan sungguh bekerja dalam misi di Divisi Antar-Amerika, yang
		meliputi Kosta Rika dan Guatemala. Dia secara khusus ingin orang-orang tahu bahwa Tuhan secara aktif mencari
		hati anak-anak dan orang muda, termasuk mereka yang akan menerima manfaat dari Persembahan Sabat Ketiga Belas
		hari ini. Sebagian dari persembahan ini akan membantu membuka pusat pengaruh untuk membagikan kasih Yesus kepada
		anak-anak yang berisiko di Kosta Rika. Proyek-proyek Sabat Ketiga Belas lainnya termasuk dua pusat pengaruh
		untuk menjangkau anak-anak yang berisiko di Kolombia, dua pusat pengaruh untuk menjangkau masyarakat kelas atas
		di Meksiko, dan pembangunan sekolah dasar yang besar di Dominika. Terima kasih atas persembahan Anda yang murah
		hati hari ini.
	</p>
