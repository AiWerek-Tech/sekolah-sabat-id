	<p>etika Stephanie dan calon suaminya mulai membicarakan tentang keinginan untuk membentuk sebuah keluarga di
		negara asalnya, Kosta Rika, mereka menyadari bahwa mereka memiliki kesamaan. Mereka berdua ingin memiliki dua
		anak kandung dan mengadopsi anak ketiga.<br /><br />Namun, lima tahun setelah pernikahan mereka, pasangan ini
		belum juga memiliki anak. Jadi, mereka memutuskan untuk mengadopsi seorang bayi.<br /><br />Namun, agen adopsi
		Kosta Rika memberi tahu mereka bahwa hal itu tidak akan mudah. Mereka harus menunggu dalam antrean yang panjang,
		dan tidak ada jaminan bahwa mereka akan mendapatkan seorang bayi. Anak yang ditawarkan untuk diadopsi boleh jadi
		remaja.<br /><br />Beberapa bulan kemudian, Stephanie hamil seorang anak perempuan. Tiga tahun kemudian, dia
		melahirkan seorang anak laki-laki. Sembilan tahun berlalu, dan dia dan suaminya teringat akan keinginan mereka
		untuk mengadopsi seorang anak. Tetapi siapa? Kapan? Di mana? Dan bagaimana caranya?<br /><br />"Tidak masalah,
		saya akan berdoa," kata Stephanie kepada suaminya. "Saya akan meminta Tuhan agar anak itu datang kepada kita dan
		bukankita yang mencari anak itu." <br /><br />Stephanie berdoa. Kemudian dia melupakannya.<br /><br />Pada suatu
		hari Sabat, Stephanie memberikan pelajaran Alkitab kepada seorang pengunjung yang baru pertama kali datang ke
		gereja pada hari itu.<br /><br />Wanita itu setuju, dan berkata, "Datanglah ke rumah saya. Saya adalah ibu dari
		10 orang anak."<br /><br />Ternyata wanita itu adalah seorang pengasuh di sebuah panti asuhan. Panti asuhan itu
		terdiri dari 15 rumah dengan 10 anak di setiap rumah. Seorang pengasuh mengawasi setiap
		rumah.<br /><br />Stephanie pergi ke panti asuhan tersebut dan memberikan pelajaran Alkitab kepada pengasuh dan
		10 anaknya. Pengasuh tersebut memberikan hatinya kepada Yesus dan dibaptis.<br /><br />Pimpinan panti asuhan,
		yang melihat bahwa pelajaran Alkitab yang diberikan berdampak positif bagi pengasuh dan pekerjaannya, meminta
		Stephanie untuk memberikan pelajaran Alkitab di 14 rumah lainnya di panti asuhan tersebut. Stephanie
		menyampaikan.permintaan tersebut kepada dewan gereja, dan gereja mengirimkan anggota jemaatnya ke 14 rumah
		tersebut. Stephanie ditugaskan di Rumah No. 7.<br /><br />Michelle yang berusia sebelas tahun tinggal di Rumah
		No. 7.<br /><br />Sejak hari pertama, Michelle menarik perhatian Stephanie. Dia membawakan tas Stephanie. Dia
		sangat perhatian, dan dia berpartisipasi dalam pelajaran Alkitab. Wanita dan gadis itu membentuk ikatan yang
		erat.<br /><br />Dengan izin dari panti asuhan, Stephanie membawa Michelle ke pertemuan penginjilan, dan dia
		dibaptis. Kemudian gadis itu mulai bertanya, "Mengapa Anda tidak mengadopsi saya?"<br /><br />Stephanie
		berpikir, "Mengapa saya tidak mengadopsinya?" Di rumah, dia memberi tahu suaminya tentang permintaan itu.
		Pasangan itu bertanya-tanya apakah Michelle mungkin adalah anak yang mereka doakan untuk diadopsi. Mereka
		meminta petunjuk dari Tuhan.<br /><br />Kemudian Stephanie teringat akan sebuah pengalaman yang terjadi tiga
		atau empat tahun sebelumnya, jauh sebelum ia bertemu dengan Michelle. Dia telah mengundang seorang wanita untuk
		membagikan kesaksian pribadinya di gereja. Wanita itu telah mengunjungi berbagai gereja untuk menceritakan
		kisahnya tentang masuk penjara, kehilangan enam anaknya karena pelayanan sosial, dan menjadi seorang Advent
		setelah mendengarkan radio Advent di penjara. Wanita itu tidak pernah datang ke gereja Stephanie, dan Stephanie
		telah kehilangan kontak dengannya.<br /><br />Sekarang Stephanie bertanya-tanya apa yang telah terjadi pada
		wanita itu. Dia melihat di media sosial dan melihat bahwa wanita itu telah meninggalkan gereja Advent. Ketika
		dia menelusuri akun media sosial wanita itu, dia melihat foto Michelle dengan tulisan,
		"Bayiku."<br /><br />Stephanie sangat terkejut. Wanita itu adalah ibu dari Michelle di panti
		asuhan.<br /><br />Beberapa hari kemudian, Stephanie melihat Michelle di sebuah pesta ulang tahun di Rumah No.
		7. Selama pesta, Michelle bertanya apakah dia bisa meminjam telepon Stephanie, dengan mengatakan, "Apakah kamu
		ingin bertemu dengan ibuku?" Ketika Stephanie mengangguk, gadis itu menemukan foto ibunya secara online dan
		menunjukkannya kepada Stephanie. Wanita itu adalah wanita yang diundang Stephanie untuk berbicara di
		gerejanya.<br /><br />Bagi Stephanie, itu semua adalah konfirmasi yang dia butuhkan. Dia yakin bahwa Tuhan telah
		mengirimkan Michelle kepada keluarganya untuk diadopsi.<br /><br />Stephanie berbicara dengan pimpinan panti
		asuhan tentang proses adopsi. Dia meyakinkan sang direktur bahwa dia tidak tahu kalau teman media sosialnya
		adalah ibu dari Michelle saat pertama kali datang ke panti asuhan. Direktur tersebut meyakinkan Stephanie bahwa
		tidak akan ada masalah jika dia tidak pernah menghubungi ibunya lagi.<br /><br />Dalam waktu singkat, Michelle
		pindah ke rumah barunya. Saat itu dia berusia 12 tahun. Butuh waktu empat tahun lagi baginya untuk diadopsi
		secara resmi, tetapi itu tidak masalah. Dia sudah merasa seperti di rumah sendiri.<br /><br />Saat ini,
		Stephanie sangat bahagia. Impiannya telah menjadi kenyataan. Dia memiliki dua anak kandung dan seorang anak
		perempuan yang diadopsi.<br /><br />Kisah misi ini memberikan gambaran kehidupan di Kosta Rika dan tantangan
		misi di sana. Persembahan Sabat Ketiga Belas Sabat berikutnya akan membantu membuka pusat pengaruh di mana kasih
		Yesus dapat dibagikan kepada anak-anak yang bermasalah, termasuk anak-anak yatim piatu.</p>
