	<p>i rumah sendirian saat malam hari, Faith yang berusia 11 tahun berhenti menjahit kancing baju seragam
		sekolahnya. Dia mengambil gunting tajam dan perlahan-lahan mengiris lengannya hingga darah mengucur. Kepalanya
		terasa ringan, dan dia mengira akan pingsan.<br />&nbsp;<br />Kehidupan Faith mulai mengalami keterpurukan
		beberapa bulan sebelumnya ketika ibunya menjalani operasi darurat. Ketika ibu menjalani pemulihan di rumah
		sakit, gadis itu ditinggal sendirian di rumah selama enam bulan di Pulau Trinidad,
		Karibia.<br />&nbsp;<br />Biasanya, kakak atau tetangga yang lebih tua akan datang untuk membantu dalam situasi
		seperti itu. Tetapi Faith tidak memiliki saudara laki-laki atau perempuan. Para tetangga tidak tahu bahwa dia
		berada di rumah sendirian, dan dia tidak pernah berpikir untuk memberi tahu mereka.<br />&nbsp;<br />Ayahnya,
		yang tinggal di tempat lain dan tidak terlibat dalam kehidupannya, berkunjung sekali atau dua kali
		sepekan.<br />&nbsp;<br />"Bagaimana kabarmu?" tanyanya setiap saat.<br />&nbsp;<br />"Saya baik-baik saja,"
		Faith menjawab.<br />&nbsp;<br />Kadang-kadang dia memberinya uang, lalu pergi. Faith tidak bercerita tentang
		kehidupannya. Dia berpikir bahwa ayahnya tidak akan mengerti.<br />&nbsp;<br />Secara otodidak, Faith belajar
		memasak nasi dan kacang-kacangan. Ketika persediaan makanan habis, dia hidup hanya makan roti, mentega, dan keju
		yang dibelinya dengan uang ayah dan sedikit uang yang ditinggalkan ibu di rumah. Dia belajar menjahit sendiri
		agar bisa memperbaiki pakaiannya.<br />&nbsp;<br />Pada siang hari, dia bersekolah di sekolah khusus perempuan.
		Dia telah menjadi target perundungan sebelumnya, tetapi masalahnya menjadi lebih buruk setelah ibunya dirawat di
		rumah sakit. Anakanak perempuan mengejek Faith tentang berat badan, penampilan, dan kecerdasannya. Pada awalnya,
		Faith menanggapinya dengan baik, berharap mereka akan menjadi teman. Namun tidak ada yang berubah. Harga dirinya
		pun semakin menurun, dan ia mulai membenci dirinya sendiri.<br />&nbsp;<br />Suatu ketika, ia dapat mengunjungi
		ibu ketika seseorang memberinya tumpangan mobil ke rumah sakit, yang berjarak 30 menit perjalanan. Ibu terlihat
		sangat lemah, pucat, dan tidak berdaya. Ketika Faith melihatnya, ia langsung menangis. la ingin menceritakan
		perundungan yang dialaminya kepada sang ibu, namun tidak bisa.<br />&nbsp;<br />Di sekolah, perundungan semakin
		meningkat. Suatu hari, seorang gadis berkata, "Kamu harus bunuh diri. Dengan begitu, semua orang akan
		bahagia."<br />&nbsp;<br />Faith memercayainya. Ia berpikir bahwa ia memang pantas dihina. la berpikir bahwa ia
		tidak pantas mendapatkan cinta, teman, atau apa pun yang baik. la mulai menyakiti dirinya sendiri. la membiarkan
		dirinya kelaparan. Dia memanjangkan kukunya dan menggaruk-garuk tubuhnya sampai berdarah. Kemudian dia beralih
		ke silet, pisau, dan gunting. Dia ingin merasakan sesuatu, apa pun. Nilainilainya turun dari B ke
		F.<br />&nbsp;<br />Lalu tibalah malam di mana dia memutuskan untuk bunuh diri dengan menggunakan
		gunting.<br />&nbsp;<br />Saat ia merasa pusing, ia mendengar sebuah suara di telinganya. Itu adalah suara
		ibunya. Sudah lama ia tidak mendengar suara itu.<br />&nbsp;<br />"Aku membutuhkanmu," kata suara itu. "Aku
		membutuhkanmu lebih dari siapa pun, bahkan jika tidak ada orang lain yang membutuhkanmu."<br />&nbsp;<br />Pada
		saat itu, Faith memutuskan untuk tidak melakukan bunuh diri. Dia akan hidup untuk ibu. Dia memaksa dirinya untuk
		bangun dan membalut lengannya.<br />&nbsp;<br />Keesokan harinya, sang guru melihat perban itu dan berbicara
		dengan Faith secara pribadi.<br />&nbsp;<br />"Apakah kamu baik-baik saja?" tanyanya.<br />&nbsp;<br />Faith
		mulai menangis dan menjerit. Segala sesuatu yang telah tertimbun di dalam dirinya selama berbulan-bulan akhirnya
		meluap. Sang guru mulai menangis.<br />&nbsp;<br />"Apakah kamu sudah makan?" tanyanya.
		.<br />&nbsp;<br />"Belum, dan saya kelaparan." "Apakah kamu sudah tidur?" "Tidak, saya tidak bisa tidur tanpa
		ibu saya."<br />&nbsp;<br />Sejak hari itu, sang guru mulai menjaga Faith di bawah pengawasannya. Di bawah
		pengawasannya, perundungan mereda dan akhirnya berhenti.<br />&nbsp;<br />Kemudian ibu pulang ke rumah. Hari itu
		adalah hari yang luar biasa! Faith menceritakan semuanya kepada ibu. Saat dia mendengarkan, ibu mulai menangis.
		Sambil menggenggam tangan Faith, ia berkata, "Maukah kamu berjanji bahwa kamu tidak akan menyakiti dirimu lagi
		dengan cara apa pun? Berjanjilah bahwa kamu tidak akan mencoba untuk memisahkan dirimu dari Ibu. Aku
		membutuhkanmu dan mencintaimu."<br />&nbsp;<br />Faith, sambil menangis, berkata, "Saya
		berjanji."<br />&nbsp;<br />Karena nilainya yang buruk, Faith harus mengulang kelas yang sama di tahun
		berikutnya. Dia kesal, tetapi ibu mengatakan bahwa itu adalah yang terbaik. Dan memang benar. Dia meninggalkan
		teman-teman sekelasnya yang lama dan dikelilingi oleh teman-teman sekelas baru yang mencintai dan menghargai
		dia. Dia mendapatkan banyak teman baik.<br />&nbsp;<br />Ibu mulai pergi ke gereja, dan dia mengajak Faith untuk
		bergabung dengannya. Keduanya tidak pernah pergi ke gereja-selama bertahuntahun. Pada awalnya, Faith tidak
		tertarik karena dia pikir itu akan membosankan. Tetapi dia pergi dan menikmati nyanyian dan paduan suara. Jadi,
		dia terus datang dan tidak berhenti. Baik dia maupun ibunya telah memberikan hati mereka kepada Yesus melalui
		baptisan.<br />&nbsp;<br />Melihat ke masa lalu, ia percaya bahwa Tuhan telah menyelamatkan hidupnya dengan
		mengirimkan suara ibunya pada saat ia membutuhkan. "Saya tidak bisa menjelaskannya," katanya. "Saya berada di
		rumah sendirian, dan saya tahu bahwa saya mendengar suara ibu saya dari dalam benak saya. Saya kira dia adalah
		orang yang paling saya butuhkan saat itu. Itu pasti Roh Kudus. Dia tahu apa yang saya
		butuhkan."<br />&nbsp;<br />Kepada anak-anak muda yang juga menghadapi perundungan, ia berkata, "Jika saya bisa
		memiliki mesin waktu dan kembali ke usia 11 tahun, saya akan berkata pada diri saya sendiri, 'Kamu akan
		baik-baik saja. Semuanya akan baik-baik saja. Kamu akan bertemu dengan banyak orang baik yang akan mencintaimu
		dan menghargaimu apa adanya. Yang harus kamu lakukan adalah mengutamakan Tuhan dan kamu akan baik-baik
		saja."<br />&nbsp;<br />Kisah misi ini memberikan pandangan dari dalam tentang kehidupan di Trinidad dan
		tantangan-tantangan misi di sana.<br />&nbsp;<br />Sebagian dari Persembahan Sabat Ketiga Belas tahun 2018
		digunakan untuk membangun sebuah gereja di Universitas Karibia Selatan, yang terletak di dekat rumah Faith.
		Adventist Mission bertemu dengan Faith untuk mendengarkan ceritanya di universitas.<br />&nbsp;<br />Terima
		kasih atas Persembahan Sabat Ketiga Belas Anda pada triwulan ini yang akan membantu memberikan harapan bagi kaum
		muda di Divisi Antar-Amerika.</p>

