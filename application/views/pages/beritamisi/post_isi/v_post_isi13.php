	<h3>Fakta Singkat</h3>
	<ul>
		<li>Hampir 90 persen spesies kupukupu Amerika Tengah dapat ditemukan di Kosta Rika. Negara ini adalah rumah bagi
			sekitar 18 persen kupu-kupu dunia.</li>

		<li>Pada tahun 2012, pemerintah Kosta Rika memberikan pengakuan khusus pada musik Calypso sebagai bagian dari
			budaya negara dan identitas sejarah.</li>

		<li>Beras dan kacang-kacangan disertakan di hampir setiap makanan Kosta Rika, terutama sarapan. Hidangan
			nasional Kosta Rika adalah gallo pinto, yang berarti "ayam jantan tutul", dan terdiri dari nasi dan
			kacang-kacangan ditumis bersama dalam panci untuk menciptakan tampilan berbintik-bintik.</li>
	</ul>
