	<p>arlin mengenali Flor saat mereka berpapasan di jalan. Sebagai seorang mantan polisi, Harlin tahu bahwa Flor akan
		melakukan apa saja demi mendapatkan uang untuk membeli narkoba dan alkohol.<br /><br />Flor juga mengenali
		Harlin. Kedua wanita itu tinggal hanya dipisah oleh satu blok di sebuah kota di Kosta Rika. Mereka telah saling
		bertegur sapa selama bertahun-tahun, tetapi tidak pernah lebih dari itu.<br /><br />Pada hari itu, Harlin ingin
		merasa ingin mengatakan lebih dari sekadar &ldquo;halo&rdquo;.<br /><br />&ldquo;Apa kabar?&rdquo;
		tanyanya.<br /><br />&ldquo;Bagaimana kehidupanmu?&rdquo;<br /><br />&ldquo;Semuanya buruk,&rdquo; jawab Flor,
		dengan wajah murung.<br /><br />Harlin menatap wanita itu dengan saksama.<br /><br />"Apakah kamu punya sesuatu
		untuk dimakan?" tanyanya.<br /><br />Flor menunduk dan mulai menangis.<br /><br />Harlin berjalan ke toko, dan
		dia pun membelikan makanan untuk Flor.<br /><br />Dalam perjalanan pulang ke rumah setelah berbelanja, ia mampir
		ke rumah Flor dan meninggalkan sekantong beras, sekantong kacang-kacangan, dan dua potong
		ayam.<br /><br />Keesokan harinya, Harlin bertemu dengan pendeta gerejanya di luar bank.<br /><br />"Saya punya
		sedikit makanan di sini," kata pendeta itu sambil menunjuk ke sebuah tas yang berisi beras, kacang-kacangan,
		minyak, tepung, dan bahan-bahan lainnya. "Apakah Anda mengetahui seseorang yang
		membutuhkannya?"<br /><br />"Ya," kata Harlin. "Saya punya tetangga yang tidak punya apa-apa untuk
		dimakan."<br /><br />"Ambillah ini dan berikan kepadanya," katanya.<br />Harlin mengundang pendeta itu untuk
		pergi bersamanya.<br /><br />"Ikutlah dengan saya, dan Anda akan melihat di mana dia tinggal,"
		katanya.<br /><br />Mereka berdua pergi ke rumah Flor dan memberikan makanan itu. <br /><br />Flor sangat
		berterima kasih.<br /><br />Ketika mereka berbicara, Harlin bertanya apakah dia ingin belajar Alkitab. Flor
		mengatakan bahwa dia mau.<br /><br />Keesokan harinya, Harlin datang untuk belajar Alkitab bersama seorang pria
		dan tiga wanita dari gerejanya. Rumah Flor terlalu usang untuk berkumpul di dalam, jadi mereka duduk di halaman
		depan rumahnya yang kecil.<br /><br />Setelah itu, kelompok tersebut bertemu sekali sepekan untuk bernyanyi dan
		mempelajari Alkitab. Flor suka bernyanyi, dan ia sangat menyukai lagu yang berbunyi, "Berikanlah aku hati yang
		baru."<br /><br />Hati Flor mulai berubah. la berhenti menghina dan memukul anggota keluarga dan tetangga yang
		membuatnya kesal. Perkataannya yang penuh dengan kata-kata kasar menjadi lebih santun.<br /><br />Dua bulan
		setelah pendalaman Alkitab, musim hujan di Kosta Rika dimulai, dan kelompok itu tidak bisa lagi bertemu di
		halaman rumah Flor karena mereka akan kehujanan.<br /><br />Flor merindukan pertemuan mingguan, dan dia mencari
		teman dari anggota gereja di Gereja Masehi Advent Hari Ketujuh pada hari Sabat.<br /><br />Tidak ada yang
		mengundangnya ke gereja. la datang sendiri.<br /><br />Dua bulan berlalu, dan pendeta gereja itu bertanya kepada
		Flor apakah ia ingin memberikan hatinya kepada Yesus melalui baptisan.<br /><br />la menyambut baik pertanyaan
		itu. "Ya," katanya. "Belum pernah ada orang yang bertanya kepada saya apakah saya ingin
		dibaptis."<br /><br />Beberapa saat kemudian, Flor dibaptiskan dalam air pertobatan.<br /><br />Flor memiliki
		reputasi yang dikenal luas di kota. Semua orang tahu kehidupannya. Penduduk kota telah melihat bagaimana ia
		berubah, dan mereka kagum melihatnya berjalan pulang dengan sukacita setelah dibaptis. Satu demi satu orang
		memeluk dan mengucapkan selamat kepadanya.<br /><br />Flor sangat bahagia! Sudah bertahun-tahun ia tidak
		mendapatkan pelukan.<br /><br />Beberapa bulan setelah pembaptisannya, Flor tetap menjadi pengikut Gereja Masehi
		Advent Hari Ketujuh yang setia di Kosta Rika.<br /><br />"Saya bersyukur kepada Tuhan atas kehidupan baru saya,"
		katanya. "Saya bersyukur atas segala sesuatu yang anggota gereja lakukan untuk saya."<br /><br />Harlin
		bersyukur atas Roh Kudus yang telah mentransformasi kehidupan Flor.<br /><br />Dia juga percaya bahwa Roh.
		Kuduslah yang mendorongnya untuk mengatakan lebih dari sekadar "halo" pada hari ketika Flor tidak memiliki
		makanan. "Tuhan itu hebat," kata Harlin. "Tidak ada yang begitu sulit yang tidak dapat Dia
		lakukan."<br /><br />Kisah misi ini memberikan pandangan dari dalam tentang kehidupan di Kosta Rika dan
		tantangan-tantangan misi di sana.<br /><br />Sebagian dari Persembahan Sabat Ketiga Belas triwulan ini akan
		membantu membuka pusat pengaruh yang akan membagikan kasih Yesus kepada anak-anak yang bermasalah terjerat dalam
		kehidupan narkoba dan alkohol.<br /><br />Terima kasih telah merencanakan Persembahan Sabat Ketiga Belas yang
		murah hati pada tanggal 28 September.</p>
