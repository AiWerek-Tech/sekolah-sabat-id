	<p>ahiquel adalah seorang anak laki-laki miskin yang tinggal di sebuah keluarga miskin di Kosta Rika. Saat tumbuh
		dewasa, dia tidak memiliki banyak hal. Namun saat berusia 16 tahun, dia mulai mengenakan rantai emas tebal di
		lehernya dan cincin emas di jari-jarinya.<br /><br />Kelompok geng di lingkungannya menjadi curiga. Pemimpin
		geng, yang dijuluki "si Iblis," mengira Jahiquel mungkin mengedarkan narkoba tanpa persetujuannya. Dia
		menginstruksikan seorang anggota geng untuk menghubungi Jahiquel melalui media sosial dan bertanya, "Apakah
		kalung Anda terbuat dari emas asli?"<br /><br />Jahiquel merasa bangga karena ia memiliki perhiasan emas asli,
		dan ia pun mengirimkan balasan satu kata, "Ya!" Dia tahu bahwa pesan itu berasal dari seorang anggota geng. Dia
		pernah menjadi anggota geng tersebut, tetapi dia telah meninggalkannya untuk bergabung dengan geng lain.
		Sekarang, alih-alih menjual narkoba, dia merampok rumah dan melakukan penculikan untuk mendapatkan uang tebusan.
		Dia membeli perhiasan emasnya dengan uang hasil kejahatan dari geng tersebut.<br /><br />Beberapa jam kemudian,
		pada pukul 7 malam, Jahiquel melihat anggota geng tersebut melaju dengan sepeda motor ke arahnya. Jahiquel
		sedang berdiri di luar rumah neneknya, di mana dia baru saja tiba untuk bermalam. la baru saja berjalan kaki
		dari rumah ibunya, tempat ia tinggal di siang hari.<br /><br />Seorang anggota geng yang duduk di belakang
		sepeda motor adalah saudara laki-lakinya. Dalam keadaan takut, Jahiquel melihat saudara laki-lakinya mengangkat
		pistol 9mm dan mulai menembak. Dia mengenali pistol itu. Dia telah menjualnya kepada saudara laki-lakinya
		beberapa bulan sebelumnya.<br /><br />Secara berurutan, delapan peluru menembus tubuh Jahiquel: dua di kakinya
		dan lima di dada dan perutnya. Dia jatuh ke tanah, ketakutan. Dia terlalu takut untuk berteriak atau bergerak.
		Berbagai pikiran memenuhi benaknya. Dia ingat, sebagai seorang anak lakilaki, dia diajari oleh bibinya yang
		merupakan seorang anggota Gereja Masehi Advent Hari Ketujuh untuk berdoa dan membaca Alkitab. Dia menyadari
		bahwa dia telah melakukan kesalahan dengan ikut serta dalam geng. Dia berdoa, "Tuhan, ampunilah
		saya."<br /><br />Waktu seakan berhenti. Kemudian ia mendengar suara motor melaju kencang. Sebuah mobil lewat
		tetapi tidak berhenti. Nenek keluar dari rumah dan membungkuk menghampiri Jahiquel.<br /><br />Mobil kedua
		datang dan berhenti. Dua orang keluar, meletakkan Jahiquel di dalam mobil, dan membawanya ke rumah
		sakit.<br /><br />Jahiquel terbangun keesokan harinya pada pukul 3 sore. Alat bantu pernapasan terpasang di
		tubuhnya. Dia mendapat 23 jahitan di bagian tengah perutnya. Setelah itu, ia mengetahui bahwa ia hampir mati,
		anggota Gereja Masehi Advent Hari Ketujuh dari gereja bibinya telah berdoa selama berjam-jam untuk
		keselamatannya. Sekarang dia masih hidup, dan itu adalah sebuah keajaiban.<br /><br />Sejak hari itu, ia
		memutuskan untuk hidup bagi Yesus.<br /><br />Tiga tahun kemudian, Jahiquel belajar untuk menjadi seorang tukang
		cukur. Dia membagikan kisahnya kepada anak-anak muda lainnya, mendorong mereka untuk menemukan makna hidup dari
		Tuhan dan bukan melalui geng. Dia mendorong mereka untuk mengisi pikiran mereka dengan hal-hal yang baik dan
		tetap dekat dengan Tuhan.<br /><br />Dalam kehidupannya sendiri, dia menyesal telah mengisi pikirannya dengan
		hal-hal yang tidak berguna ketika remaja, dan dia percaya bahwa Tuhan akan mengisinya dengan hal-hal yang baik
		ketika dia menghabiskan waktu dengan mempelajari Alkitab. Lagi pula, Rasul Paulus berkata, "Janganlah kamu
		menjadi serupa dengan dunia ini, tetapi berubahlah oleh pembaharuan budimu, supaya kamu dapat membedakan manakah
		kehendak Allah: apa yang baik, yang berkenan kepada Allah dan yang sempurna" (Roma 12: 2).<br /><br />"Saya
		ingin memikirkan hal-hal yang baik, dan saya tidak ingin mengingat masa lalu," kata Jahiquel. "Sekarang saya
		membaca Alkitab setiap pagi dan malam. Saya bangun lebih awal untuk berdoa. Ini adalah sebuah proses. Saya tahu
		bahwa dengan pertolongan Tuhan, saya bisa melangkah lebih jauh."<br /><br />Dua saudara laki-laki yang
		menyerangnya tidak pernah diadili karena dia tidak mengungkapkan identitas mereka kepada polisi. Dia masih
		tinggal di lingkungan yang sama, dan dia mengerti bahwa melaporkan mereka akan membahayakan dirinya, ibunya, dan
		neneknya.<br /><br />Sedangkan untuk perhiasan emasnya, dia kehilangan semuanya pada hari terjadinya
		penyerangan. Dia tidak tahu siapa yang mengambilnya, tetapi dia tidak keberatan. Dia telah menemukan bahwa harta
		yang sesungguhnya tidak bisa dilihat. Harta yang sesungguhnya adalah apa yang ada di dalam
		hatinya.<br /><br />Kisah misi ini memberikan pandangan dari dalam tentang kehidupan di Kosta Rika dan tantangan
		misi di sana.<br /><br />Sebagian dari Persembahan Sabat Ketiga Belas triwulan ini akan membantu membuka pusat
		pengaruh untuk membagikan kasih Yesus kepada anak-anak seperti Jahiquel, yang berisiko terpengaruh oleh narkoba
		dan geng di Kosta Rika.<br /><br />Terima kasih telah merencanakan persembahan yang murah hati pada tanggal 28
		September.</p>
