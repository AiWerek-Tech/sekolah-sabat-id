	<p>driana sangat mahir dalam merintis gereja-gereja di Kolombia. Istri dan ibu berusia 35 tahun ini telah
		mendirikan enam gereja selama 15 tahun, dan ia siap untuk membantu mendirikan gereja No. 7 di sebuah lingkungan
		miskin di selatan ibu kota Kolombia yang luas, Bogota.<br /><br />Namun, tidak ada seorang pun yang dapat
		menemukan sebuah bangunan yang terjangkau di lokasi yang strategis.<br /><br />Benih untuk gereja ditanam ketika
		Wilinton, seorang mantan pecandu alkohol di lingkungan itu, memberikan hatinya kepada Yesus dan memulai sebuah
		kelompok belajar Alkitab kecil di rumahnya. Kelompok ini berkembang melebihi rumahnya dan pindah beberapa kali.
		Sekarang 50 orang beribadah bersama setiap hari Sabat, dan sudah waktunya bagi mereka untuk memiliki gedung
		gereja sendiri.<br /><br />Adriana adalah anggota gereja lain yang ingin membantu jemaat Wilinton untuk
		mendirikan gereja. Dia melihat lima bangunan dan tidak menemukan apa pun.<br /><br />Kemudian Wilinton
		menyarankan sebuah bar yang sering ia kunjungi sebelum ia dibaptis. Bar itu berada tepat di tengah-tengah
		lingkungan, dan akan dijual. Tetapi harga yang diminta terlalu tinggi.<br /><br />Adriana bertemu dengan pemilik
		bar tersebut. la menjelaskan bahwa para anggota gereja ingin membeli bangunan itu tetapi tidak memiliki
		dana.<br /><br />"Bisakah Anda menunggu selama satu tahun sementara kami mengumpulkan uang?"
		tanyanya.<br /><br />Pemiliknya tidak mau menunggu, dan dia menggelengkan kepalanya.<br /><br />Adriana memohon
		agar dia mempertimbangkannya kembali, tetapi dia menolak. Namun, dia menambahkan, "Jika rencana Anda berasal
		dari Tuhan, bangunan ini akan menjadi milik Anda."<br /><br />Adriana dan anggota gereja lainnya berdoa kepada
		Tuhan untuk meminta pertolongan, dan mereka mulai mengumpulkan uang.<br /><br />Kemudian seorang calon pembeli
		datang untuk melihatlihat bangunan itu. Dia menyukai apa yang dilihatnya, dan dia setuju untuk
		membelinya.<br /><br />Para anggota gereja bertanyatanya apakah mereka akan kehilangan gedung itu. Mereka berdoa
		agar kehendak Tuhan terjadi.<br /><br />Calon pembeli pergi dan tidak pernah kembali dengan membawa uang untuk
		membeli gedung tersebut.<br /><br />Bangunan itu tetap dijual. Kemudian seorang pembeli lain mengunjungi gedung
		itu dan menawarkan diri untuk membelinya. Sepertinya penjualan itu akan berhasil. Para anggota gereja kembali
		berdoa agar kehendak Tuhan terjadi, dan pembeli tersebut tidak kembali dengan membawa
		uangnya.<br /><br />Pemilik bar tidak mengerti mengapa bangunannya tidak laku. la menurunkan harganya. Kemudian
		ia menurunkannya lagi. Namun tetap saja, ia tidak dapat mendapatkan pembeli.<br /><br />Adriana yakin bahwa
		Tuhan memiliki rencana untuk mengubah bar tersebut menjadi sebuah gereja dan pusat pengaruh. Pusat pengaruh itu,
		akan mengajarkan keterampilan hidup kepada orang-orang di masyarakat dan membantu mereka yang berjuang melawan
		kecanduan alkohol.<br /><br />Adriana mengunjungi pemilik bar lagi.<br /><br />"Anda tidak akan menjual bangunan
		ini," katanya dengan tegas. "Anda akan menjualnya kepada kami karena ini akan menjadi tempat yang akan membantu
		masyarakat dan memuliakan Tuhan."<br /><br />Setahun berlalu, dan para anggota gereja telah mengumpulkan cukup
		uang untuk membeli bangunan tersebut dengan harga yang lebih rendah. Seorang pemimpin gereja mengunjungi
		pemiliknya untuk menyelesaikan penjualan.<br /><br />Pemiliknya merasa lega karena akhirnya bisa menjual
		bangunan tersebut.<br /><br />"Saya tidak bisa menjual bangunan ini karena orang-orang Advent telah berdoa untuk
		membelinya," katanya.<br /><br />Dua pekan kemudian, bangunan itu dijual kepada Gereja Masehi Advent Hari
		Ketujuh. Harga akhir yang dikurangi adalah setengah dari nilai pasar bangunan serupa di lingkungan
		tersebut.<br /><br />Saat ini, Adriana memuji Tuhan atas gereja dan pusat pengaruh yang baru.<br /><br />"Kami
		semua berdoa untuk tempat ini," katanya. "Ketika Tuhan memiliki rencana, tidak ada yang bisa
		mengubahnya."<br /><br />Kisah misi ini memberikan pandangan dari dalam tentang kehidupan di Kolombia dan
		tantangan-tantangan misi di sana.<br /><br />Sebagian dari Persembahan Sabat Ketiga Belas triwulan ini akan
		membantu membuka dua pusat pengaruh untuk membantu anakanak yang bermasalah mengalami penyalahgunaan alkohol dan
		obatobatan di Kolombia.<br /><br />Terima kasih telah merencanakan persembahan yang murah hati pada tanggal 28
		September.</p>

