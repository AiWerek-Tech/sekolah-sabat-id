	<p>ehidupan Wilinton mulai berubah setelah ia berhenti minum-minum di Bogota, Kolombia. Dia mendapatkan pekerjaan,
		dia mulai melunasi utangutangnya, dan dia menikahi istri dari iparnya. Kemudian Wilinton, istrinya, dan putra
		mereka yang berusia 12 tahun memutuskan untuk menyerahkan hati mereka kepada Tuhan dalam
		baptisan.<br /><br />Sehari sebelum Wilinton dibaptiskan, seorang teman dari Gereja Masehi Advent Hari Ketujuh
		mengatakan bahwa Tuhan memiliki rencana besar baginya. "Kamu akan menjadi Rasul Paulus berikutnya," kata
		temannya, Jaime. "Kamu akan menolong banyak orang." 2024<br /><br />Tidak butuh waktu lama bagi Wilinton
		menyadari bahwa Tuhan memanggilnya untuk menolong para pecandu alkohol.<br /><br />Sebulan setelah dibaptis,
		ketika ia sedang berjalan ke gereja pada hari Sabat pagi, ia mendengarkan sebuah suara yang berkata, "Berhenti
		di sini."<br /><br />la berhenti. Ketika melihat ke atas, ia melihat sebuah jalan kota yang dipenuhi dengan
		bar-bar tempat ia biasa minum-minum.<br /><br />Suara itu berkata, "Kamu akan menolong orang-orang ini. Kamu
		pernah mabuk, dan sekarang kamu akan menolong orang lain yang mabuk. Aku akan memberimu kesempatan, dan aku akan
		membantumu untuk tidak kecanduan lagi."<br /><br />Di gereja, Wilinton mencari Jaime. "Aku punya ide,"
		katanya.<br /><br />"Aku juga punya ide," kata Jaime. "Ceritakan idemu terlebih dahulu."<br /><br />"Aku pikir
		Tuhan menyuruh aku untuk menjadi terang di dalam jeruji besi yang hanya ada kegelapan," kata Wilinton. "Wow!"
		Jaime berseru. "Itu adalah ide yang sama dengan yang aku miliki."<br /><br />Tampaknya hal itu lebih dari
		sekadar kebetulan. Kedua orang itu sepakat bahwa Roh Kudus pasti memanggil Wilinton untuk menolong para pecandu
		alkohol.<br /><br />Selama pekan berikutnya, Wilinton mengundang orang-orang yang berada di bar-bar datang ke
		rumahnya untuk belajar Alkitab. Sebelas orang hadir pada pelajaran Alkitab pertama pada hari Sabat
		sore.<br /><br />Setelah beberapa pekan berlalu, kelompok itu menjadi terlalu besar untuk bertemu di rumahnya.
		Wilinton menyewa sebuah rumah yang lebih besar. Ketika kelompok itu menjadi semakin banyak, mereka pindah ke
		sebuah aula. Orang-orang dibaptiskan, dan kelompok itu terus berkembang.<br /><br />Akhirnya, Wilinton
		memutuskan bahwa kelompok itu perlu memiliki tempat sendiri. la mencari sebuah bangunan untuk dibeli, tetapi
		harga tanah di kota sangat mahal. Ketika ia berdoa, ia merasa tertarik untuk mengunjungi sebuah bar yang sering
		ia kunjungi sejak usia 16 tahun. Tampaknya mustahil, tetapi jika ia dapat membeli bar tersebut, bar itu dapat
		direnovasi menjadi gedung pertemuan.<br /><br />Dia berdoa, "Tuhan, apakah Engkau benar-benar ingin aku bekerja
		untuk-Mu melalui sebuah bar?"<br /><br />Wilinton mencari pemilik bar tersebut dan mengetahui bahwa bangunan itu
		akan dijual.<br /><br />Pemiliknya segera mengenali Wilinton, dan dia terkejut melihatnya dalam keadaan
		sadar.<br /><br />"Apakah itu benar-benar Anda?" tanyanya. "Ya, itu aku," kata Wilinton.<br /><br />"Apakah Anda
		ingat bagaimana Anda sering datang ke sini untuk minum-minum?" katanya.<br /><br />"Ya, aku
		ingat."<br /><br />Pemiliknya menggelengkan kepalanya karena tidak percaya. "Aku mengenal Anda," katanya. "Aku
		tidak percaya bahwa Anda memiliki kehidupan yang baru."<br /><br />Dia menyebutkan harga untuk bangunan
		itu.<br /><br />Wilinton tidak memiliki uang sebanyak itu. la memberi tahu anggota gereja yang lain tentang
		bangunan itu, tetapi mereka juga tidak memiliki dana.<br /><br />Wilinton tidak patah semangat. "Rencana Allah
		itu sempurna," katanya kepada mereka. "Jangan khawatir. Gedung ini akan menjadi milik kita."<br /><br />Dia dan
		anggota jemaat lainnya mulai berdoa dan mengumpulkan dana. Setahun kemudian, mereka membeli bar tersebut dan
		mengubahnya menjadi sebuah gereja Advent dan pusat pengaruh.<br /><br />Saat ini, 50 orang beribadah di gereja
		tersebut setiap hari Sabat. Selama sepekan, pusat pengaruh ini menawarkan kelas-kelas keterampilan hidup dan
		program untuk berhenti dari minuman keras.<br /><br />Wilinton, yang merupakan penatua gereja dan wakil direktur
		pusat pengaruh, mengatakan bahwa, melalui kelompok kecilnya, lebih dari 15 mantan pecandu alkohol telah
		menyerahkan hati mereka kepada Yesus dan dibaptis. Selain itu, 85 orang telah dibaptis melalui gereja baru dan
		pusat pengaruh selama tiga tahun terakhir.<br /><br />"Sekarang aku bekerja untuk Yesus dan mencari mereka yang
		hilang," kata Wilinton. "Aku telah memutuskan untuk menjadi seperti Paulus, selalu membagikan Injil dan meminta
		kuasa Tuhan untuk memimpin orang-orang kepada-Nya."<br /><br />la menambahkan, "Rencana Allah itu sempurna, dan
		tidak pernah berubah."<br /><br />Kisah misi ini memberikan pandangan dari dalam tentang kehidupan di Kolombia
		dan tantangan-tantangan misi di sana.<br /><br />Sebagian dari Persembahan Sabat Ketiga Belas triwulan ini akan
		membantu membuka dua pusat pengaruh untuk membantu anak-anak yang bermasalah mengalami penyalahgunaan alkohol
		dan narkoba di Kolombia. Pusat-pusat pengaruh tersebut akan berada di Kota Buenaventura dan Puerto
		Tejada.<br /><br />Terima kasih telah merencanakan persembahan yang murah hati pada tanggal 28 September.</p>

